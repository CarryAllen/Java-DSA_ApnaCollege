public class Variables {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);

        int b = 5;
        System.out.println(b);
        
        String name = "Tony Stark";
        System.out.println(name);

        a = 50;
        System.out.println(a);
    }
    
}
